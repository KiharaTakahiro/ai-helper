"""
パイプライン実行コンテキスト。

------------------------------------------------------------
■ このモジュールの役割
------------------------------------------------------------
Node（処理単位）同士の間で「データの受け渡し」を行うための
共通コンテナ（箱）を提供する。

各 Node は直接データを渡し合うのではなく、
この Context を経由してデータを共有する。

------------------------------------------------------------
■ なぜ必要なのか？
------------------------------------------------------------
Node は独立した処理として設計されているため、
直接データを受け渡すと結合が強くなる。

そこで、

    Node A → Context → Node B

という構造にすることで、

- Node同士の依存を弱くする（疎結合）
- テストしやすくする
- 再利用しやすくする

------------------------------------------------------------
■ 実際のデータの流れ（イメージ）
------------------------------------------------------------

Node A:
    "image" → "artifact_id_123" を生成
    ↓
Context に保存
    context.set_artifact("image", "artifact_id_123")

Node B:
    context.get_artifact("image") で取得
    ↓
"artifact_id_123" を使ってデータを読み込む

------------------------------------------------------------
■ ポイント
------------------------------------------------------------
- Context 自体は「データ本体」を持たない
- 持つのは「アーティファクトID（参照）」のみ
- 実データは ArtifactRepository が管理する
"""

from typing import Dict


class Context:
    """
    パイプライン実行時に使用される共有コンテキスト。

    ------------------------------------------------------------
    ■ このクラスの役割
    ------------------------------------------------------------
    ノード間でアーティファクトIDを受け渡すための
    「名前付き辞書」を提供する。

    ------------------------------------------------------------
    ■ 重要な設計ポイント
    ------------------------------------------------------------
    このクラスは「データ本体」を保持しない。

    代わりに：

        "論理名" → "アーティファクトID"

    のマッピングだけを保持する。

    実際のデータは ArtifactRepository に保存されるため、
    Context は軽量で高速に扱える。

    ------------------------------------------------------------
    ■ Attributes
    ------------------------------------------------------------
    artifacts (Dict[str, str]):

        アーティファクトの名前とIDの対応表

        例:
            {
                "input_text": "artifact_001",
                "summary": "artifact_002"
            }
    """

    def __init__(self):
        """
        Context を初期化する。

        初期状態では何もアーティファクトは登録されていない。
        """
        self.artifacts: Dict[str, str] = {}

    def set_artifact(self, artifact_name: str, artifact_id: str):
        """
        アーティファクトIDをコンテキストに保存する。

        ------------------------------------------------------------
        ■ 何をしているか
        ------------------------------------------------------------
        指定された名前でアーティファクトIDを登録する。

        ------------------------------------------------------------
        ■ なぜ必要か
        ------------------------------------------------------------
        Node の出力結果を「名前付き」で保存することで、
        後続の Node がその名前で参照できるようになる。

        ------------------------------------------------------------
        ■ 上書きについて
        ------------------------------------------------------------
        同じ名前が既に存在する場合は上書きされる。

        これは以下のようなケースを想定している：

        - 同じ名前で再計算する場合
        - パイプライン内で値を更新する場合

        Args:
            artifact_name (str):
                アーティファクトの論理名（例: "image", "text"）

            artifact_id (str):
                ArtifactRepository に保存されているID
        """

        self.artifacts[artifact_name] = artifact_id

    def get_artifact(self, artifact_name: str) -> str:
        """
        指定した名前のアーティファクトIDを取得する。

        ------------------------------------------------------------
        ■ 何をしているか
        ------------------------------------------------------------
        指定された名前に対応するアーティファクトIDを返す。

        ------------------------------------------------------------
        ■ なぜ必要か
        ------------------------------------------------------------
        Node はこのIDを使って ArtifactRepository から
        実際のデータを取得する。

        ------------------------------------------------------------
        ■ エラーについて
        ------------------------------------------------------------
        指定した名前が存在しない場合は KeyError を発生させる。

        これは「入力が不足している」状態であり、
        パイプラインの設計ミスである可能性が高いため、
        明示的にエラーにする。

        Args:
            artifact_name (str):
                取得したいアーティファクトの名前

        Returns:
            str:
                アーティファクトID

        Raises:
            KeyError:
                指定した名前が存在しない場合
        """

        return self.artifacts[artifact_name]